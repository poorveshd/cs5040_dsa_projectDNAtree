/**
 * Node class is an abstract class acting
 * as base for all the nodes.
 * 
 * @author Gautam Sharma <gautams@vt.edu>
 * @author Poorvesh Dongre <poorvesh@vt.edu>
 * @version 3/9/2022
 */
public abstract class Node {
    /**
     * Test for checking if a node is leaf or not.
     *
     * @return boolean value denoting whether node
     *         is a leaf or not
     */
    public abstract Boolean isLeaf();
}
